
public class MinHeapNode {
	
	private char data;
	private int freq;
	
	public MinHeapNode(char data, int freq) {
		super();
		this.data = data;
		this.freq = freq;
	}

	public int getFreq() {
		return freq;
	}

	public void setFreq(int freq) {
		this.freq = freq;
	}

	public char getData() {
		return data;
	}

	public void setData(char data) {
		this.data = data;
	}
	
	
	
	
}
